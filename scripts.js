//Toggle function for SMALL SCREEN


$(document).ready(function () {
  $(".toggle-1").click(function () {
    $(".first").toggle(1000);
    $(".second").hide(200);
    $(".third").hide(200);
    $(".fourth").hide(200);
    $(".fifth").hide(200);
    $(".sixth").hide(200);
  });
});



